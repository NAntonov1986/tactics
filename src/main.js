/* main.js (src/) — точка входа игры. Финальная сессия R19.

   До R19 эта строка жила в inline `<script>` внутри index.html — последняя
   функционально-нагруженная строка монолита, оставшаяся после переезда
   всего остального в `src/data/*`, `src/core/*`, `src/render/*`, `src/ui/*`.
   После R19 inline-блок удалён целиком, и `init()` вызывается отсюда —
   из последнего `<script src="src/main.js">` в `index.html`.

   Ничего другого здесь не нужно: вся бизнес-логика, рендер, ввод и камера
   живут в специализированных модулях. main.js — это эквивалент `if __name__ == '__main__'`:
   единственное, что он делает — стартует игру, когда DOM и все 25 предыдущих
   script-ов уже загружены.

   Тонкость с порядком загрузки. Файл должен подключаться ПОСЛЕДНИМ среди
   `<script src>` в `index.html` — после всех data/, core/, render/, ui/.
   `init()` сам определён в `core/state.js`, но дёргает изнутри функции
   из всех слоёв (createInitialState, startNextWave, bindFieldClickHandler,
   bindActionPanelHandler, bindHotkeys, setupViewInteractions, render,
   applyView). К моменту выполнения этого `init();` все они уже есть
   в script-scope.

   Что НЕ внутри:
     • Сама функция `init()` — `core/state.js`. Здесь только её ВЫЗОВ.
     • Регистрация обработчиков (`bind*`) — это всё внутри `init()`.
     • Любая бизнес-логика — `data/*`, `core/*`, `render/*`, `ui/*`.

   Где править:
     • Если когда-то понадобится async-инициализация (preload спрайтов,
       fetch конфига, await save-load) — оборачивать `init()` в async-функцию
       и менять только эту строку. Сценарий бутстрапа сам по себе живёт
       в `init()` в state.js — там его и расширять.
     • Хуки «после старта» (трекер аналитики, hot-reload в dev-режиме) —
       тоже сюда, после `init();`. Если их станет много (>3 строк) —
       завести отдельный `bootstrap.js` или `analytics.js`. */


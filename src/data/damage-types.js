/* damage-types.js — справочники типов урона и хелпер-описание.
   Что внутри:
     • DELIVERY_TYPES — способ доставки урона (melee / ranged / aoe).
       Поля: label (полное название), short (короткое для тултипов).
     • DAMAGE_TYPES — сам тип урона (physical / fire / frost / electric /
       poison / magic / holy / special).
       Поля: label, color (для визуального выделения в логе/тултипах).
       У `special` есть флаг bypassesResistance: true — задел под будущую
       систему сопротивлений (этот тип их игнорирует).
     • describeDamage(delivery, damageType) — собирает строку вида
       «ближний · физический» для лога и тултипов. Если ключ незнаком
       — выводит сам ключ как fallback.
   Что НЕ внутри:
     • UNIT_TYPES (типы существ) — отдельный файл src/data/unit-types.js.
     • Конкретное оружие со своими типами — src/data/weapons.js.
     • Иммунитеты/сопротивления по типу цели — src/core/damage.js.
   Где править подписи/цвета конкретного типа: тут.
   Где добавить новый тип урона: добавить запись в DAMAGE_TYPES, далее
     убедиться, что все источники урона корректно его поддерживают
     (по умолчанию — да, всё считается через ключ).
   Файл подключается ОБЫЧНЫМ <script src="..."> до inline-блока в index.html.
   Все определения (DELIVERY_TYPES, DAMAGE_TYPES, describeDamage) попадают
   в глобальный scope window — это намеренно, как у текущего монолита.
*/

/* ================================================================
   === ТИПЫ УРОНА =================================================
   ================================================================
   Каждая атака описывается двумя компонентами:
     delivery — способ доставки урона: ближний / дальний / по площади.
     damageType — физический/огненный/... Потом от типа будут зависеть
     сопротивления, иммунитеты и взаимодействия с эффектами.
   Справочники тут — чтобы в одном месте править подписи/цвета и
   гарантировать, что строковые ключи не плавают по коду.
   ================================================================ */
const DELIVERY_TYPES = {
  melee:     { label: 'ближний',         short: 'ближн.'  },
  ranged:    { label: 'дальний',         short: 'дальн.'  },
  aoe:       { label: 'по площади',      short: 'по площ.' },
  // Сессия 13/14: не атакующие delivery — телепорт (перемещение
  // самого юнита) и self_buff (бафф на самого мага или союзника).
  // describeDamage используется в тултипах слотов скиллов и в логе
  // боя; без записей здесь fallback вывел бы английский ключ.
  teleport:  { label: 'перенос',         short: 'перенос' },
  self_buff: { label: 'свой/союзный бафф', short: 'бафф' },
  cleanse:   { label: 'снятие эффектов', short: 'очищ.' },
  // Camp v1.5-priest-B (10.05.2026): таргет на надгробие (Воскрешение).
  // Не атака — поэтому строка «Тип» в тултипе для этого delivery
  // подавляется (см. фильтр в skill-tooltip.js / render-panel.js).
  grave_target: { label: 'на надгробие', short: 'могила' }
};
const DAMAGE_TYPES = {
  // С24-рефактор: добавлено поле `short` — компактная форма для тесных
  // тултипов (raison d'être: «электрический → морозный → огненный»
  // в подписи призматической сферы — слишком длинно). По умолчанию
  // используется label (см. describeDamage в логе). short читает только
  // render-panel в специфичных тултипах (strikes, breakdown'ы).
  physical: { label: 'физический',    short: 'физ.',    color: '#c8c8c8' },
  fire:     { label: 'огненный',      short: 'огонь',   color: '#ff7a33' },
  frost:    { label: 'морозный',      short: 'холод',   color: '#7acfe8' },
  electric: { label: 'электрический', short: 'электр.', color: '#f5d84a' },
  poison:   { label: 'ядовитый',      short: 'яд',      color: '#7bd25a' },
  magic:    { label: 'магический',    short: 'магия',   color: '#c080ff' },
  holy:     { label: 'святой',        short: 'свет',    color: '#ffd24c' },
  special:  { label: 'специальный',   short: 'спец.',   color: '#e74fb8', bypassesResistance: true }
};
function describeDamage(delivery, damageType) {
  const d = DELIVERY_TYPES[delivery]; const t = DAMAGE_TYPES[damageType];
  return `${d ? d.label : delivery} · ${t ? t.label : damageType}`;
}
